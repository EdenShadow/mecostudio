/**
 * 解析命令行参数
 */
function parseArgs(args) {
  const options = {
    category: 'general',
    depth: CONFIG.defaultDepth,
    context: ''
  };
  
  const queryParts = [];
  let i = 0;
  
  while (i < args.length) {
    const arg = args[i];
    
    if (arg === '--help' || arg === '-h') {
      showHelp();
      process.exit(0);
    } else if ((arg === '--category' || arg === '-c') && i + 1 < args.length) {
      options.category = args[++i];
    } else if ((arg === '--depth' || arg === '-d') && i + 1 < args.length) {
      options.depth = args[++i];
    } else if ((arg === '--context' || arg === '-x') && i + 1 < args.length) {
      options.context = args[++i];
    } else if ((arg === '--output' || arg === '-o') && i + 1 < args.length) {
      CONFIG.knowledgeBaseDir = args[++i];
    } else if (!arg.startsWith('-')) {
      queryParts.push(arg);
    }
    i++;
  }
  
  options.query = queryParts.join(' ');
  return options;
}

/**
 * 主程序
 */
async function main() {
  const args = process.argv.slice(2);
  
  // 无参数显示帮助
  if (args.length === 0) {
    showHelp();
    process.exit(0);
  }
  
  const command = args[0];
  
  try {
    // 处理子命令
    if (command === 'index') {
      await generateSearchIndex();
      process.exit(0);
    }
    
    if (command === 'guide') {
      await generateQuickGuide();
      process.exit(0);
    }
    
    if (command === 'list') {
      await listKnowledgeBase();
      process.exit(0);
    }
    
    // 搜索命令
    const options = parseArgs(args);
    
    if (!options.query) {
      console.error('❌ 错误: 请提供搜索内容');
      showHelp();
      process.exit(1);
    }
    
    // 创建知识库目录
    await fs.mkdir(CONFIG.knowledgeBaseDir, { recursive: true });
    
    // 执行搜索
    const result = await kimiSearch(options.query, options);
    
    // 保存结果
    await saveSearchResult(options.query, result, options.category);
    
    // 更新索引和指南
    await generateSearchIndex();
    await generateQuickGuide();
    
    console.log('\n✅ 搜索完成！');
    console.log(`📚 知识库位置: ${CONFIG.knowledgeBaseDir}`);
    console.log(`📑 使用 'kimi-search index' 查看索引`);
    console.log(`📖 使用 'kimi-search guide' 查看使用指南`);
    
  } catch (error) {
    console.error(`\n❌ 错误: ${error.message}`);
    process.exit(1);
  }
}

// 运行主程序
main();
