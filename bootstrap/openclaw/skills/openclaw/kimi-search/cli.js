#!/usr/bin/env node

/**
 * Kimi Search CLI - 使用Kimi AI进行联网搜索并构建本地知识库
 * 
 * 功能：
 * 1. 使用kimi_cli进行联网搜索
 * 2. 整理搜索结果并保存到本地
 * 3. 生成知识库索引和快捷说明书
 * 4. 适用于创建Agents、查找人设信息、构建知识库等场景
 */

import { spawn } from 'child_process';
import { promises as fs } from 'fs';
import { homedir } from 'os';
import path from 'path';

// 配置
const CONFIG = {
  // 知识库默认保存位置
  knowledgeBaseDir: process.env.KIMI_SEARCH_DIR || path.join(homedir(), 'Desktop', 'kimi-knowledge-base'),
  // 默认搜索深度
  defaultDepth: 'standard', // 'quick', 'standard', 'deep'
  // 最大重试次数
  maxRetries: 2,
  // kimi cli 路径
  kimiPath: 'kimi'
};

/**
 * 执行kimi命令进行搜索
 * @param {string} query - 搜索查询
 * @param {Object} options - 搜索选项
 * @returns {Promise<string>} - 搜索结果
 */
async function kimiSearch(query, options = {}) {
  const { depth = CONFIG.defaultDepth, context = '' } = options;
  
  // 构建搜索提示词
  let searchPrompt = buildSearchPrompt(query, depth, context);
  
  console.log(`🔍 正在使用Kimi进行搜索: "${query}"`);
  console.log(`📊 搜索深度: ${depth}`);
  
  return new Promise((resolve, reject) => {
    let output = '';
    let errorOutput = '';
    
    // 使用kimi -p参数直接传递搜索提示
    const kimiArgs = [
      '-p', searchPrompt,
      '-y' // 自动确认
    ];
    
    const kimi = spawn(CONFIG.kimiPath, kimiArgs, {
      cwd: process.cwd(),
      env: process.env
    });
    
    kimi.stdout.on('data', (data) => {
      const chunk = data.toString();
      output += chunk;
      process.stdout.write(chunk);
    });
    
    kimi.stderr.on('data', (data) => {
      const chunk = data.toString();
      errorOutput += chunk;
      process.stderr.write(chunk);
    });
    
    kimi.on('close', (code) => {
      if (code !== 0 && code !== null) {
        reject(new Error(`Kimi exited with code ${code}: ${errorOutput}`));
      } else {
        resolve(output);
      }
    });
    
    kimi.on('error', (error) => {
      reject(new Error(`Failed to start kimi: ${error.message}`));
    });
    
    // 设置超时（5分钟）
    setTimeout(() => {
      kimi.kill();
      reject(new Error('Search timeout after 5 minutes'));
    }, 5 * 60 * 1000);
  });
}

/**
 * 构建搜索提示词
 */
function buildSearchPrompt(query, depth, context) {
  const depthInstructions = {
    quick: '请进行快速搜索，提供核心信息摘要（约500字）。',
    standard: '请进行标准搜索，提供详细但简洁的信息（约1000-1500字）。',
    deep: '请进行深入搜索，提供全面的信息、多个角度和细节（约2000-3000字）。'
  };
  
  let prompt = `请帮我搜索以下信息："${query}"

搜索要求：
${depthInstructions[depth] || depthInstructions.standard}

输出格式要求：
1. 使用清晰的Markdown格式
2. 包含关键信息、数据、时间线
3. 如果有多个相关主题，请分点列出
4. 注明信息来源（网站名称或类型）
5. 提供可用于进一步搜索的关键词
`;

  if (context) {
    prompt += `\n\n额外背景信息：${context}\n`;
  }
  
  prompt += `
请确保信息准确、完整，适合用于构建知识库。如果搜索结果不足，请说明并提供可能的替代搜索词。
`;

  return prompt;
}

/**
 * 保存搜索结果到文件
 */
async function saveSearchResult(query, result, category = 'general') {
  const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
  const safeQuery = query.replace(/[^a-zA-Z0-9\u4e00-\u9fa5]/g, '_').substring(0, 50);
  
  // 创建目录结构
  const categoryDir = path.join(CONFIG.knowledgeBaseDir, category);
  await fs.mkdir(categoryDir, { recursive: true });
  
  // 保存搜索结果
  const resultFile = path.join(categoryDir, `${safeQuery}_${timestamp}.md`);
  const content = `# ${query}\n\n**搜索时间**: ${new Date().toLocaleString()}\n**分类**: ${category}\n\n---\n\n${result}\n\n---\n\n*Generated by kimi-search*\n`;
  
  await fs.writeFile(resultFile, content, 'utf8');
  console.log(`\n💾 搜索结果已保存: ${resultFile}`);
  
  return resultFile;
}

/**
 * 生成搜索索引
 */
async function generateSearchIndex() {
  try {
    const entries = await fs.readdir(CONFIG.knowledgeBaseDir, { withFileTypes: true });
    const categories = entries.filter(e => e.isDirectory()).map(e => e.name);
    
    let indexContent = `# Kimi 知识库索引\n\n**知识库位置**: ${CONFIG.knowledgeBaseDir}\n**最后更新**: ${new Date().toLocaleString()}\n\n## 📂 分类目录\n\n`;
    
    for (const category of categories) {
      const categoryPath = path.join(CONFIG.knowledgeBaseDir, category);
      const files = await fs.readdir(categoryPath);
      const mdFiles = files.filter(f => f.endsWith('.md') && !f.startsWith('_'));
      
      indexContent += `### ${category} (${mdFiles.length} 个文件)\n\n`;
      
      for (const file of mdFiles) {
        const filePath = path.join(categoryPath, file);
        const content = await fs.readFile(filePath, 'utf8');
        const title = content.split('\n')[0].replace('# ', '');
        const dateMatch = content.match(/\*\*搜索时间\*\*: (.+)/);
        const date = dateMatch ? dateMatch[1] : '未知';
        
        indexContent += `- [${title}](./${category}/${file}) - ${date}\n`;
      }
      
      indexContent += '\n';
    }
    
    indexContent += `\n---\n\n*使用 \`kimi-search index\` 更新此索引*\n`;
    
    const indexFile = path.join(CONFIG.knowledgeBaseDir, '_INDEX.md');
    await fs.writeFile(indexFile, indexContent, 'utf8');
    console.log(`📑 索引已更新: ${indexFile}`);
    
    return indexFile;
  } catch (error) {
    console.error('生成索引失败:', error.message);
    return null;
  }
}

